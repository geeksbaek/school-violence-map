function makeRegions(): Region[] {
  const out: Region[] = [];
  // 서울
  for (const [sgg, name] of SEOUL_DISTRICTS) {
    out.push({
      sido: "11",
      sgg,
      sgg10: sgg + "00000",
      city: "서울특별시",
      district: name,
      label: `서울 ${name}`,
    });
  }
  // 경기
  for (const [city, integ, subs] of GG_CITIES) {
    if (integ) {
      out.push({
        sido: "41",
        sgg: integ,
        sgg10: integ + "00000",
        city,
        district: subs.length === 0 ? "" : "(통합)",
        label: subs.length === 0 ? city : `${city} (통합)`,
      });
    }
    for (const [sgg, name] of subs) {
      out.push({
        sido: "41",
        sgg,
        sgg10: sgg + "00000",
        city,
        district: name,
        label: `${city} ${name}`,
      });
    }
  }
  return out;
}

